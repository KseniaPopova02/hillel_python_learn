def callendar(m):
    if m == 1:
        return ("январь")
    elif m == 2:
        return ("февраль")
    elif m == 3:
        return ("март")
    elif m == 4:
        return ("апрель")
    elif m == 5:
        return ("май")
    elif m == 6:
        return ("июнь")
    elif m == 7:
        return ("июль")
    elif m == 8:
        return ("август")
    elif m == 9:
        return ("сентябрь")
    elif m == 10:
        return ("октябрь")
    elif m == 11:
        return ("ноябрь")
    elif m == 12:
        return ("декабрь")


print(callendar(11))
